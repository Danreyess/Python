#Ciclos: repeticion eficiente
#Ciclo for: Se usa cuando sabes cuantas veces se debe repetir algo. Es ideal para recorrer listas o rangos de numeros

#Imprimir los numeros del 0 al 4
for i in range(5):
    print(f"Iteracion numero: {i}")

#Recorrer una lista de tus herramientas
herramientas = ["Pandas", "Numpy", "Scikit-learn"]
for h in herramientas:
    print(f"Estoy aprendiendo: {h}")

