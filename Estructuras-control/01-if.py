#Condicionales: Tomando decisiones
#(If, elif, else)

edad = int(input("Edad: "))
if edad >= 18:
    print("Puedes entrar al club")
elif edad >= 15:
    print("Casi, vuelve en unos años")
else:
    print("Lo siento, eres muy joven")

